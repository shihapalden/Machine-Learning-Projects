import xgboost as xgb
import pandas as pd
from sklearn.preprocessing import LabelEncoder
from sklearn.model_selection import train_test_split
from sklearn.metrics import mean_squared_error

data = pd.read_csv('salary.csv')
x = data[['Gender','EducationLevel','YearsExperience']]
y= data['Salary']

Label_encoder = LabelEncoder()
x['Gender'] = Label_encoder.fit_transform(x['Gender'])
x['EducationLevel'] = Label_encoder.fit_transform(x['EducationLevel'])
X_train, X_test, Y_train, Y_test = train_test_split(x,y,test_size=0.2)

model = xgb.XGBRegressor()
model.fit(X_train, Y_train)

y_pred = model.predict(X_test)
mse = mean_squared_error(Y_test, y_pred)
print(mse)

new_data = pd.DataFrame({
    'Gender': ['Male'],
    'EducationLevel': ['Master'],
    'YearsExperience': [5]
})

new_data['Gender'] = Label_encoder.fit_transform(new_data['Gender'])
new_data['EducationLevel'] = Label_encoder.fit_transform(new_data['EducationLevel'])

predicted_salary = model.predict(new_data)
print("Predicted Salary:", predicted_salary[0])